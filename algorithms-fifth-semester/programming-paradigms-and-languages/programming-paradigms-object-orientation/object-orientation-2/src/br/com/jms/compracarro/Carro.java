package br.com.jms.compracarro;

public class Carro {
    String marca;
    String modelo;
    String placa;
    Integer ano;

    public Carro(String marca, String modelo, String placa, Integer ano) {
        this.marca = marca;
        this.modelo = modelo;
        this.placa = placa;
        this.ano = ano;
    }
}
